fun main() {
    val day = "Monday"
    when (day) {
        "Monday" -> println("MON")
        "Tuesday" -> println("TUE")
        "Wednesday" -> println("WED")
        "Thursday" -> println("THU")
        "Friday" -> println("FRI")
        "Saturday" -> println("SAT")
        "Sunday" -> println("SUN")
        else -> println("Invalid day")
    }
}